public class SnmpApStatusInfo {
	private String apIPv4;
	private String apIPv6;
	private String apMac;
	private String apTemplateName;
	private String apConnectionType;
	private String apStatus;
	public String getApIPv4() {
		return apIPv4;
	}
	public void setApIPv4(String apIPv4) {
		this.apIPv4 = apIPv4;
	}
	public String getApIPv6() {
		return apIPv6;
	}
	public void setApIPv6(String apIPv6) {
		this.apIPv6 = apIPv6;
	}
	public String getApMac() {
		return apMac;
	}
	public void setApMac(String apMac) {
		this.apMac = apMac;
	}
	public String getApTemplateName() {
		return apTemplateName;
	}
	public void setApTemplateName(String apTemplateName) {
		this.apTemplateName = apTemplateName;
	}
	public String getApConnectionType() {
		return apConnectionType;
	}
	public void setApConnectionType(String apConnectionType) {
		this.apConnectionType = apConnectionType;
	}
	public String getApStatus() {
		return apStatus;
	}
	public void setApStatus(String apStatus) {
		this.apStatus = apStatus;
	}
	
	
}